"use client";

// Branding Studio Phase 4 — the on-demand brand-template gallery. Lives in
// Settings → Brand Kit, below the brand-kit form (which owns the editable style
// guide: colors / fonts / voice / headline). This panel ONLY generates + lists
// marketing templates (social post / story / poster) via gpt-image-1. Generation
// is real (paid) image work, so it's button-triggered, never automatic. Gated
// upstream by ai_images_enabled + the monthly spend cap — we surface those.

import React, { useCallback, useEffect, useState } from "react";
import { Loader2, Sparkles, Download, RefreshCw, ImageIcon, Megaphone } from "lucide-react";
import toast from "react-hot-toast";
import { Button } from "@/components/ui/button";
import {
  listBrandTemplates, generateBrandTemplates,
  type BrandTemplate, type TemplateKind,
} from "@/services/api/aiBrandKit";

const KIND_META: Record<TemplateKind, { label: string; ratio: string; aspect: string }> = {
  social_post: { label: "Social Post", ratio: "1:1 · 1080×1080", aspect: "aspect-square" },
  social_story: { label: "Social Story", ratio: "9:16 · story", aspect: "aspect-[9/16]" },
  poster: { label: "Poster", ratio: "landscape", aspect: "aspect-[3/2]" },
};
const ALL_KINDS: TemplateKind[] = ["social_post", "social_story", "poster"];

export const BrandTemplatesPanel: React.FC = () => {
  const [templates, setTemplates] = useState<BrandTemplate[]>([]);
  const [loading, setLoading] = useState(true);
  const [generating, setGenerating] = useState<null | "all" | TemplateKind>(null);
  const [prompt, setPrompt] = useState("");

  const load = useCallback(async () => {
    setLoading(true);
    try {
      setTemplates(await listBrandTemplates().catch(() => []));
    } finally {
      setLoading(false);
    }
  }, []);

  useEffect(() => { void load(); }, [load]);

  const generate = async (kinds: TemplateKind[], which: "all" | TemplateKind) => {
    setGenerating(which);
    try {
      const results = await generateBrandTemplates(kinds, prompt.trim() || undefined);
      const ok = results.filter((r) => r.ok).length;
      const failed = results.filter((r) => !r.ok);
      if (ok > 0) toast.success(`Generated ${ok} template${ok > 1 ? "s" : ""}.`);
      if (failed.length) {
        const first = failed[0];
        toast.error(first.error || `Couldn't generate ${KIND_META[first.kind].label}.`);
      }
      await load();
    } catch (e: any) {
      // 403 (AI images off) / 429 (budget) come back as a rejection with message.
      toast.error(e?.message || "Couldn't generate templates. Please try again.");
    } finally {
      setGenerating(null);
    }
  };

  const byKind = (k: TemplateKind) => templates.filter((t) => t.kind === k);

  return (
    <section>
      <div className="flex items-start justify-between gap-4 mb-1">
        <h2 className="text-xl font-semibold text-[#FFCC00] flex items-center gap-2">
          <Megaphone className="w-5 h-5" /> Marketing Templates
        </h2>
        <Button
          onClick={() => generate(ALL_KINDS, "all")}
          disabled={generating !== null || loading}
          className="bg-[#FFCC00] text-black hover:bg-[#E6B800] font-medium shrink-0"
        >
          {generating === "all" ? <Loader2 className="w-4 h-4 animate-spin" /> : <Sparkles className="w-4 h-4" />}
          {generating === "all" ? "Generating…" : "Generate all"}
        </Button>
      </div>
      <p className="text-sm text-gray-400 mb-3">
        On-brand graphics generated by AI with your logo stamped on. Each one is a
        real AI image (counts toward your monthly AI budget), so generate when you
        need them.
      </p>
      <input
        type="text"
        value={prompt}
        onChange={(e) => setPrompt(e.target.value)}
        maxLength={500}
        placeholder="Tell the AI what to show — e.g. 'screen repair promo, 20% off this week'"
        className="w-full mb-2 px-3 py-2 bg-[#1A1A1A] border border-gray-700 rounded-lg text-white text-sm placeholder-gray-500 focus:outline-none focus:border-[#FFCC00] transition-colors"
      />
      <p className="text-xs text-gray-500 mb-5">
        Applies to whatever you generate below. Leave empty to use your brand profile.
        Your colors + logo are always applied.
      </p>

      {loading ? (
        <div className="flex items-center gap-2 text-gray-400 text-sm py-6">
          <Loader2 className="w-4 h-4 animate-spin" /> Loading templates…
        </div>
      ) : (
        <div className="space-y-6">
          {ALL_KINDS.map((kind) => {
            const items = byKind(kind);
            const meta = KIND_META[kind];
            return (
              <div key={kind}>
                <div className="flex items-center justify-between mb-2">
                  <div>
                    <p className="text-sm font-medium text-white">{meta.label}</p>
                    <p className="text-xs text-gray-500">{meta.ratio}</p>
                  </div>
                  <button
                    onClick={() => generate([kind], kind)}
                    disabled={generating !== null}
                    className="inline-flex items-center gap-1.5 text-xs font-medium px-3 py-1.5 rounded-md bg-[#1A1A1A] border border-gray-700 text-gray-300 hover:border-[#FFCC00] hover:text-white disabled:opacity-50 transition-colors"
                  >
                    {generating === kind ? (
                      <Loader2 className="w-3.5 h-3.5 animate-spin" />
                    ) : items.length ? (
                      <RefreshCw className="w-3.5 h-3.5" />
                    ) : (
                      <Sparkles className="w-3.5 h-3.5 text-[#FFCC00]" />
                    )}
                    {items.length ? "Generate another" : "Generate"}
                  </button>
                </div>

                {items.length === 0 ? (
                  <div className={`rounded-xl border border-dashed border-white/10 bg-[#141414] ${meta.aspect} max-w-[220px] flex flex-col items-center justify-center text-center p-4`}>
                    <ImageIcon className="w-6 h-6 text-gray-600 mb-2" />
                    <span className="text-xs text-gray-500">No {meta.label.toLowerCase()} yet</span>
                  </div>
                ) : (
                  <div className="flex flex-wrap gap-3">
                    {items.map((t) => (
                      <div key={t.id} className="group relative">
                        <img
                          src={t.url}
                          alt={`${meta.label} template`}
                          className={`${meta.aspect} w-[220px] object-cover rounded-xl border border-white/10 bg-[#141414]`}
                        />
                        <a
                          href={t.url}
                          target="_blank"
                          rel="noopener noreferrer"
                          download
                          className="absolute bottom-2 right-2 inline-flex items-center gap-1.5 text-xs font-medium px-2.5 py-1.5 rounded-md bg-black/70 text-white opacity-0 group-hover:opacity-100 transition-opacity"
                        >
                          <Download className="w-3.5 h-3.5" /> Download
                        </a>
                      </div>
                    ))}
                  </div>
                )}
              </div>
            );
          })}
        </div>
      )}
    </section>
  );
};

export default BrandTemplatesPanel;
